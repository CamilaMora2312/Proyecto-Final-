    private void TbNutMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbNutMouseClicked
        // TODO add your handling code here:
         try{
            int fila = TbNut.getSelectedRow();
            String id = TbNut.getValueAt(fila, 0).toString();
            String id_nutriologo = txtId.getText();
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id_nutriologo=?, cedula_profecional, nombre_nutriologo FROM Nutriologos");
            ps.setString(1,id_nutriologo);
            rs = ps.executeQuery();
            
            
            while(rs.next()){
                txtId.setText(String.valueOf(id));
                txtCProf.setText(rs.getString("cedula_profecional"));
                txtNombre.setText(rs.getString("nombre_nutriologo"));
                
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.toString());
    }            
    
    }//GEN-LAST:event_TbNutMouseClicked

