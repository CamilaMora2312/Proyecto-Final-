    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        int id_nutriologo =Integer.parseInt(txtId.getText());
        long  cedula_profecional =Long.parseLong(txtCProf.getText());
        String  nombre_nutriologo =txtNombre.getText();
        
        try{
              Connection con = Conexion.getConexion();
              PreparedStatement ps = con.prepareStatement("INSERT INTO Nutriologos (id_nutriologo, cedula_profecional, nombre_nutriologo)VALUES(?,?,?)");
              ps.setInt(1, id_nutriologo);
              ps.setLong(2, cedula_profecional);
              ps.setString(3, nombre_nutriologo);
              ps.executeUpdate();
              JOptionPane.showMessageDialog(null, "Nutriologo Registrado");
              Limpiar();
              cargarTabla();
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }                                          

    private void Limpiar(){
        txtId.setText("");
        txtCProf.setText("");
        txtNombre.setText("");
       
    }
     
     private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) TbNut.getModel();
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        try{
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT  id_nutriologo, cedula_profecional, nombre_nutriologo FROM Nutriologos");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while (rs.next()){
                Object[] fila =  new Object[columnas];
                for(int indice=0; indice<columnas; indice ++){
                    fila[indice] = rs.getObject(indice + 1);
                    
                }
                modeloTabla.addRow(fila);
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.toString());
       
    }                                         
    }//GEN-LAST:event_btnRegistrarActionPerformed

