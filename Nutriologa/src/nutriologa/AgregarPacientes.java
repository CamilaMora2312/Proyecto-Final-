    private void TbPacientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbPacientesMouseClicked
        // TODO add your handling code here:
         try{
            int fila = TbPacientes.getSelectedRow();
            String id = TbPacientes.getValueAt(fila, 0).toString();
            String id_paciente = txtId.getText();
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id_paciente=?, nombre_paciente, dom_paciente, tel_paciente, ocupacion_paciente, motivo_paciente, sexo_paciente, edad_paciente FROM Pacientes");
            ps.setString(1,id_paciente);
            rs = ps.executeQuery();
            
            
            while(rs.next()){
                txtId.setText(String.valueOf(id));
                txtNombre.setText(rs.getString("nombre_paciente"));
                txtDomicilio.setText(rs.getString("dom_paciente"));
                txtTelefono.setText(rs.getString("tel_paciente"));
                txtOcupacion.setText(rs.getString("ocupacion_paciente"));
                txtMotivo.setText(rs.getString("motivo_paciente"));
                txtSexo.setText(rs.getString("sexo_paciente"));
                txtEdad.setText(rs.getString("edad_paciente"));
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.toString());
    }              
    }//GEN-LAST:event_TbPacientesMouseClicked

