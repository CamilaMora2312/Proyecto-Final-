    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        // TODO add your handling code here:
        int id_consultas =Integer.parseInt(txtId.getText());
        int  id_citas =Integer.parseInt(txtIdCitas.getText());
        String  id_pacientes =txtId_paciente.getText();
        String  id_nutriologo =txtId_nutriologo.getText();
        String  fecha_consulta =txtFecha.getText();
        
        try{
              Connection con = Conexion.getConexion();
              PreparedStatement ps = con.prepareStatement("INSERT INTO Consultas (id_consultas, id_citas, id_pacientes, id_nutriologo, fecha_consulta) VALUES(?,?,?,?,?)");
              ps.setInt(1, id_consultas);
              ps.setInt(2, id_citas);
              ps.setString(3, id_pacientes);
              ps.setString(4, id_nutriologo);
              ps.setString(5, fecha_consulta);
              ps.executeUpdate();
              JOptionPane.showMessageDialog(null, "Consulta Agendada");
              Limpiar();
              cargarTabla();
        } catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }                                          

    private void Limpiar(){
        txtId.setText("");
        txtIdCitas.setText("");
        txtId_paciente.setText("");
        txtId_nutriologo.setText("");
        txtFecha.setText("");
    }
     
     private void cargarTabla(){
        DefaultTableModel modeloTabla = (DefaultTableModel) TbConsultas.getModel();
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        
        try{
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id_consultas, id_citas, id_pacientes, id_nutriologo, fecha_consulta FROM Consultas");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            
            while (rs.next()){
                Object[] fila =  new Object[columnas];
                for(int indice=0; indice<columnas; indice ++){
                    fila[indice] = rs.getObject(indice + 1);
                    
                }
                modeloTabla.addRow(fila);
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.toString());
       
    }

    }//GEN-LAST:event_btnAgendarActionPerformed

