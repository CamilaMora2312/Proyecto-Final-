    private void TbConsultasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbConsultasMouseClicked
        // TODO add your handling code here:
                try{
            int fila = TbConsultas.getSelectedRow();
            String id = TbConsultas.getValueAt(fila, 0).toString();
            String id_paciente = txtId.getText();
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConexion();
            ps = con.prepareStatement("SELECT id_consultas=?,id_citas , id_pacientes, id_nutriologo, fecha_consulta FROM Consultas");
            ps.setString(1,id_paciente);
            rs = ps.executeQuery();
            
            
            while(rs.next()){
                txtId.setText(String.valueOf(id));
                txtIdCitas.setText(rs.getString("id_citas"));
                txtId_paciente.setText(rs.getString("id_pacientes"));
                txtId_nutriologo.setText(rs.getString("id_nutriologo"));
                txtFecha.setText(rs.getString("fecha_consulta"));
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,ex.toString());
    } 
    
    }//GEN-LAST:event_TbConsultasMouseClicked

